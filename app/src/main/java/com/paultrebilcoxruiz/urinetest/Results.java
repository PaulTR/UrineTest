package com.paultrebilcoxruiz.urinetest;


public class Results {
    private String leukocytes;
    private String nitrates;
    private String ph;

    public Results() {

    }

    public String getLeukocytes() {
        return leukocytes;
    }

    public void setLeukocytes(String leukocytes) {
        this.leukocytes = leukocytes;
    }

    public String getNitrates() {
        return nitrates;
    }

    public void setNitrates(String nitrates) {
        this.nitrates = nitrates;
    }

    public String getPh() {
        return ph;
    }

    public void setPh(String ph) {
        this.ph = ph;
    }
}
